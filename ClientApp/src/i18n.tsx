const DICTS: Record<Lang, Dict> = { en, pl, ru }

interface I18nContextType {
  lang: Lang
  setLang: (l: Lang) => void
  t: (key: string) => string
  /** Translate a known backend error message; falls back to the original text. */
  te: (msg: string) => string
}

const I18nContext = createContext<I18nContextType | null>(null)

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>(
    () => (localStorage.getItem('lang') as Lang) || 'en'
  )
  useEffect(() => {
    document.documentElement.lang = lang
  }, [lang])

  const setLang = (l: Lang) => {
    localStorage.setItem('lang', l)
    setLangState(l)
  }
  const t = (key: string) => DICTS[lang][key] ?? DICTS.en[key] ?? key

  // Map English backend messages to localized text where we know them.
  const te = (msg: string) => {
    const hit = Object.keys(en).find((k) => k.startsWith('err.') && en[k] === msg)
    return hit ? t(hit) : msg
  }

  return <I18nContext.Provider value={{ lang, setLang, t, te }}>{children}</I18nContext.Provider>
}

export function useI18n(): I18nContextType {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error('useI18n must be used within I18nProvider')
  return ctx
}
